def calculate_basic_stats(data: pd.DataFrame) -> Dict[str, Any]:
    """
    محاسبه آمار پایه از داده قیمتی برای درک بستر تحلیل
    """
    # استخراج ستون‌ها
    close = data['close'].values if 'close' in data.columns else data['Close'].values
    high = data['high'].values if 'high' in data.columns else data['High'].values
    low = data['low'].values if 'low' in data.columns else data['Low'].values
    volume = data['volume'].values if 'volume' in data.columns else None
    
    n = len(close)
    
    if n == 0:
        return {"error": "داده موجود نیست"}
    
    # آمار پایه
    min_price = float(np.min(low))
    max_price = float(np.max(high))
    current_price = float(close[-1])
    first_price = float(close[0])
    
    total_change = current_price - first_price
    total_change_pct = (total_change / first_price) * 100 if first_price != 0 else 0
    
    # نوسان‌پذیری روزانه
    returns = np.diff(close) / close[:-1]
    volatility = float(np.std(returns) * 100) if len(returns) > 0 else 0
    
    # تشخیص روند کلی با میانگین متحرک ساده
    if n >= 20:
        short_ma = np.mean(close[-20:])
        long_ma = np.mean(close[-50:]) if n >= 50 else np.mean(close)
        
        if short_ma > long_ma * 1.005:
            trend = "صعودی قوی"
        elif short_ma > long_ma:
            trend = "صعودی ضعیف"
        elif short_ma < long_ma * 0.995:
            trend = "نزولی قوی"
        elif short_ma < long_ma:
            trend = "نزولی ضعیف"
        else:
            trend = "خنثی (رنج)"
    else:
        trend = "صعودی" if close[-1] > close[0] else "نزولی"
    
    # حجم معاملات (در صورت وجود)
    volume_analysis = {}
    if volume is not None and len(volume) > 0:
        avg_volume = float(np.mean(volume))
        last_volume = float(volume[-1])
        volume_analysis = {
            "میانگین_حجم": f"{avg_volume:,.0f}",
            "آخرین_حجم": f"{last_volume:,.0f}",
            "نسبت_حجم_آخرین_به_میانگین": round(last_volume / avg_volume, 2) if avg_volume > 0 else 0
        }
    
    # فاصله قیمت فعلی از بالاترین و پایین‌ترین
    drawdown_from_high = ((max_price - current_price) / max_price) * 100 if max_price > 0 else 0
    rally_from_low = ((current_price - min_price) / min_price) * 100 if min_price > 0 else 0
    
    return {
        "تعداد_کندل": n,
        "بازه_زمانی": f"{data.index[0] if hasattr(data.index[0], 'strftime') else 0} تا {data.index[-1] if hasattr(data.index[-1], 'strftime') else n}",
        "بازه_قیمتی": (round(min_price, 4), round(max_price, 4)),
        "قیمت_شروع": round(first_price, 4),
        "قیمت_پایانی": round(current_price, 4),
        "تغییر_کل": round(total_change, 4),
        "تغییر_کل_درصد": round(total_change_pct, 2),
        "نوسان‌پذیری_روزانه_درصد": round(volatility, 2),
        "روند_کلی": trend,
        "افت_از_بیشترین": round(drawdown_from_high, 2),
        "صعود_از_کمترین": round(rally_from_low, 2),
        "تحلیل_حجم": volume_analysis,
    }


