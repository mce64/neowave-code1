    def full_analysis(self, waves: List[WaveSegment], subsequent_wave: Optional[WaveSegment] = None,
                      in_triangle: bool = False) -> Dict[str, Any]:
        if len(waves) < 3:
            return {"error": "حداقل ۳ موج برای تحلیل نیاز است"}
    
        pattern_type = self.detect_pattern_type(waves, in_triangle)
    
        pattern = PatternInstance(
            pattern_type=pattern_type,
            start_price=waves[0].start.price,
            end_price=waves[-1].end.price,
            high_price=max(w.end.price for w in waves),
            low_price=min(w.end.price for w in waves),
            direction=waves[0].direction,
            is_in_triangle=in_triangle,
            is_terminal_component=False,
            is_completing_larger_pattern=False,
            time_duration=sum(w.length for w in waves),
            sub_waves=[]
        )
    
        rating = self.get_pattern_rating(pattern_type, pattern.direction, in_triangle)
    
        results = {
            "timestamp": datetime.now().isoformat(),
            "wave_count": len(waves),
            "pattern_type": pattern_type.value,
            "persian_name": pattern.persian_name,
            "rating": rating,
            "summary": {
                "persian_name": pattern.persian_name,
                "signed_rating": rating["signed_rating"],
                "absolute_rating": rating["absolute_rating"],
                "return_rule": rating["return_rule"],
                "expected_return_min": rating["expected_return_range"]["min_percent"],
                "expected_return_max": rating["expected_return_range"]["max_percent"]
            }
        }
    
        if subsequent_wave:
            results["post_pattern_verification"] = self.verify_post_pattern_return(pattern, subsequent_wave)
    
        return results
    
