class ReturnRules:
    """
    قوانین بازگشت بازار پس از الگوهای اصلاحی
    صفحه ۳۴۴
    """
    
    # درصدهای بازگشت بر اساس قدر مطلق رتبه
    RETURN_PERCENTAGES = {
        0: (0.01, 1.00),    # ۱٪ تا ۱۰۰٪
        1: (0.00, 0.90),    # کمتر از ۹۰٪
        2: (0.00, 0.80),    # کمتر از ۸۰٪
        3: (0.60, 1.00),    # حداقل ۶۰٪
    }
    
    @classmethod
    def get_expected_return_range(cls, absolute_rating: int) -> Tuple[float, float]:
        """دریافت محدوده بازگشت مورد انتظار"""
        return cls.RETURN_PERCENTAGES.get(absolute_rating, (0.0, 1.0))
    
    @classmethod
    def is_return_valid(cls, absolute_rating: int, actual_return_pct: float) -> Tuple[bool, str, str]:
        """
        بررسی اعتبار بازگشت بر اساس رتبه
        
        خروجی: (معتبر است?, پیام, توضیح فارسی)
        """
        min_ret, max_ret = cls.get_expected_return_range(absolute_rating)
        
        if absolute_rating == 0:
            if 0.01 <= actual_return_pct <= 1.00:
                return True, f"بازگشت {actual_return_pct*100:.1f}% - در محدوده مجاز (۱٪ تا ۱۰۰٪)", "رتبه ۰: هر بازگشتی از ۱٪ تا ۱۰۰٪ ممکن است"
            return False, f"بازگشت {actual_return_pct*100:.1f}% - خارج از محدوده", "رتبه ۰: بازگشت باید بین ۱٪ تا ۱۰۰٪ باشد"
        
        elif absolute_rating == 1:
            if actual_return_pct <= 0.90:
                return True, f"بازگشت {actual_return_pct*100:.1f}% - کمتر از ۹۰٪ ✓", "رتبه ۱: بازگشت باید کمتر از ۹۰٪ باشد"
            return False, f"بازگشت {actual_return_pct*100:.1f}% - باید کمتر از ۹۰٪ باشد", "رتبه ۱: بازگشت بیش از ۹۰٪ مجاز نیست"
        
        elif absolute_rating == 2:
            if actual_return_pct <= 0.80:
                return True, f"بازگشت {actual_return_pct*100:.1f}% - کمتر از ۸۰٪ ✓", "رتبه ۲: بازگشت باید کمتر از ۸۰٪ باشد"
            return False, f"بازگشت {actual_return_pct*100:.1f}% - باید کمتر از ۸۰٪ باشد", "رتبه ۲: بازگشت بیش از ۸۰٪ مجاز نیست"
        
        elif absolute_rating == 3:
            if actual_return_pct >= 0.60:
                return True, f"بازگشت {actual_return_pct*100:.1f}% - حداقل ۶۰٪ ✓", "رتبه ۳: بازگشت باید حداقل ۶۰٪ باشد"
            return False, f"بازگشت {actual_return_pct*100:.1f}% - باید حداقل ۶۰٪ باشد", "رتبه ۳: بازگشت کمتر از ۶۰٪ مجاز نیست"
        
        return True, "بدون محدودیت", "بدون محدودیت"
    
    @classmethod
    def get_pattern_power_description(cls, absolute_rating: int, direction: str, 
                                       in_triangle: bool = False) -> str:
        """
        دریافت توضیح قدرت الگو
        
        صفحه ۳۴۴:
        - پایین‌ترین رتبه (۳) برای ادامه روند صعودی بسیار منفی است
        - بالاترین رتبه (۳) برای ادامه روند صعودی بسیار مناسب است
        """
        if in_triangle:
            return "الگو درون مثلث - رتبه‌بندی خنثی شده است"
        
        if absolute_rating == 0:
            return "الگوی خنثی - رفتار پساالگویی وابسته به موقعیت"
        elif absolute_rating == 1:
            return "الگوی ضعیف - بازگشت محدود (کمتر از ۹۰٪)"
        elif absolute_rating == 2:
            return "الگوی متوسط - بازگشت نسبتاً محدود (کمتر از ۸۰٪)"
        elif absolute_rating == 3:
            if direction == "UP":
                return "الگوی بسیار قوی برای ادامه روند صعودی (حداقل ۶۰٪ بازگشت)"
            else:
                return "الگوی بسیار قوی برای ادامه روند نزولی (حداقل ۶۰٪ بازگشت)"
        return "رتبه نامشخص"


